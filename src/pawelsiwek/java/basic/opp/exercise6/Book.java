package pawelsiwek.java.basic.opp.exercise6;

public class Book {
    String author;
    String title;
    String publishingHouse;
    int yearOfPublish;

    public Book() {
    }

    public Book(String author, String title, String publishingHouse, int yearOfPublish) {
        this.author = author;
        this.title = title;
        this.publishingHouse = publishingHouse;
        this.yearOfPublish = yearOfPublish;
    }
}
